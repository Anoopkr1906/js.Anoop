// WHILE loop

let index = 0;
while(index <= 10){
    // console.log(`value of index is ${index}`);
    index ++;
}

let myArray = ["flash" , "batman" , "spiderman"];
let arr = 0;
while(arr < myArray.length){
    // console.log(`value is ${myArray[arr]}`);
    arr++;
}

// DO WHILE loop
// the loop is run once always even if condition is not met ...

let score = 1;

do{
    console.log(`value of score is ${score}`);
    score ++;
}
while(score <= 10);

